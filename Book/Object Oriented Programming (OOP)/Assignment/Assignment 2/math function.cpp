#include<iostream>
#include<math.h>

using namespace std;

int main()
{
    float x;

    cout<<"Enter the value of x:";
    cin>>x;
    cout<<"Ceil value for "<<x<<" is :"<<ceil(x)<<endl;
    cout<<"floor value for "<<x<<" is :"<<floor(x)<<endl;
    cout<<"square root value for "<<x<<" is :"<< sqrt(x)<< endl;
    cout<<"sin value for "<<x<<" is :"<< sin(x) << endl;
    cout<<"cos value for "<<x<<" is :"<< cos(x) << endl;
    cout<<"log value for "<<x<<" is :"<< log(x) << endl;

}
